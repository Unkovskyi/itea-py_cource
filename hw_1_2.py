print('{} / {} = {}'.format(num_1, num_2, (num_1 / num_2)))
print('{} / {} = {}'.format(num_2, num_1, (num_2 / num_1)))

print('{} // {} = {}'.format(num_1, num_2, (num_1 // num_2)))
print('{} // {} = {}'.format(num_2, num_1, (num_2 // num_1)))

print('{} % {} = {}'.format(num_1, num_2, (num_1 % num_2)))
